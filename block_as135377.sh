#!/bin/bash
# AS135377 拉黑 - 自包含单文件脚本（无需额外文件）
# 用法: bash block_as135377_allinone.sh
# 需 root 权限

set -e

# root 检查
[[ $EUID -ne 0 ]] && { echo "请使用 root 运行: sudo bash $0"; exit 1; }

# 缺失依赖时自动安装并重新执行
install_ipset() {
    echo "检测到 ipset 未安装，正在尝试自动安装..."
    if command -v apt-get &>/dev/null; then
        apt-get update -qq 2>/dev/null; apt-get install -y ipset
    elif command -v yum &>/dev/null; then
        yum install -y ipset
    elif command -v dnf &>/dev/null; then
        dnf install -y ipset
    elif command -v apk &>/dev/null; then
        apk add --no-cache ipset
    else
        echo "无法识别包管理器，请手动安装: apt install ipset 或 yum install ipset"
        return 1
    fi
}
if ! command -v ipset &>/dev/null; then
    if install_ipset; then
        if command -v ipset &>/dev/null; then
            echo "ipset 安装成功，重新执行脚本..."
            exec "$0"
        fi
    fi
fi

# 从本脚本 __DATA__ 到 __END__ 之间提取 CIDR
get_cidrs() {
    sed -n '/^__AS135377_DATA__$/,/^__END_DATA__$/p' "$0" | grep -oE '([0-9]{1,3}\.){3}[0-9]{1,3}/[0-9]{1,2}' | sort -u
}

CIDRS=$(get_cidrs)
COUNT=$(echo "$CIDRS" | grep -c . || true)

[[ $COUNT -eq 0 ]] && { echo "错误: 未找到 CIDR 数据"; exit 1; }

echo "共 $COUNT 个网段"

# 优先使用 ipset，无则回退到纯 iptables
if command -v ipset &>/dev/null; then
    echo "使用 ipset 模式..."
    ipset create as135377 hash:net hashsize 2048 maxelem 65536 2>/dev/null || ipset flush as135377
    echo "$CIDRS" | while read -r cidr; do
        [[ -z "$cidr" ]] && continue
        ipset add as135377 "$cidr" 2>/dev/null || true
    done
    if ! iptables -C INPUT -m set --match-set as135377 src -j DROP 2>/dev/null; then
        iptables -I INPUT -m set --match-set as135377 src -j DROP
        echo "已添加 iptables 规则"
    fi
    ipset save > /etc/ipset.conf 2>/dev/null && echo "ipset 已保存" || true
else
    echo "ipset 未安装，使用纯 iptables 模式..."
    echo "$CIDRS" | while read -r cidr; do
        [[ -z "$cidr" ]] && continue
        iptables -A INPUT -s "$cidr" -j DROP 2>/dev/null || true
    done
    echo "已添加 iptables 规则"
fi

# 持久化 iptables
mkdir -p /etc/iptables 2>/dev/null
iptables-save > /etc/iptables/rules.v4 2>/dev/null && echo "iptables 已保存" || true

echo "完成! AS135377 已拉黑"

exit 0
